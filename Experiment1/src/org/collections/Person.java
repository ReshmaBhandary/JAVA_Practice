package org.collections;

public class Person {
	private Integer phone=0;;
	private String name;
	public Person(String name) {
		this.name=name;
	}
	public Integer getPhone() {
		return phone;
	}
	public void setPhone(Integer phone) {
		this.phone = phone;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
